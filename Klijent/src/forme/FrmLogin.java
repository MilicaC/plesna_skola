    private void ulogujSe(String username, String password) throws Exception {
        Administrator admin = new Administrator();
        admin.setUsername(username);
        admin.setPassword(password);
        admin = Kontroler.getInstanca().nadjiAdmina(admin);
        ulogovaniAdmin = admin;
        Sesija.getInstanca().getMapa().put("admin", admin);   
    }
