    private void btnPrijaviSeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijaviSeActionPerformed
        
        String username = txtUsername.getText();
        char[] pass = txtPassword.getPassword();
        String password = String.copyValueOf(pass);
        
        try {
            if((txtUsername.getText().equals("Korisničko ime"))&& (String.valueOf(txtPassword.getPassword()).equals("Lozinka"))){
            JOptionPane.showMessageDialog(this, "Korisničko ime i lozinka ne smeju biti prazne vrednosti.", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
                return;
        }
            if((txtUsername.getText().equals("Korisničko ime")) && (!String.valueOf(txtPassword.getPassword()).equals("Lozinka"))){
                JOptionPane.showMessageDialog(this, "Korisničko ime i lozinka ne smeju biti prazne vrednosti.", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if((!txtUsername.getText().equals("Korisničko ime")) && (String.valueOf(txtPassword.getPassword()).equals("Lozinka"))){
                JOptionPane.showMessageDialog(this, "Korisničko ime i lozinka ne smeju biti prazne vrednosti.", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(!username.isEmpty() && !password.isEmpty()){
            ulogujSe(username, password);
            JOptionPane.showMessageDialog(this, "Administrator je uspešno ulogovan.", "Uspešno!", JOptionPane.INFORMATION_MESSAGE);
            FrmGlavna fg = new FrmGlavna(ulogovaniAdmin);
            fg.setVisible(true);
            this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Korisničko ime i lozinka ne smeju biti prazne vrednosti.", "Upozorenje!", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Došlo je do greške. Unesite ponovo podatke.", "Greška!", JOptionPane.ERROR_MESSAGE);
            txtUsername.setText("Korisničko ime");
            txtPassword.setText("Lozinka");
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnPrijaviSeActionPerformed

