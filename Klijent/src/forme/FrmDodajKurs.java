    private void btnObrisiPlesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiPlesActionPerformed
        int red = tabelaPlesovi.getSelectedRow();
        if(red == -1){
            JOptionPane.showMessageDialog(this, "Морате изабрати ред.", "Упозорење!",JOptionPane.INFORMATION_MESSAGE);
        }else{
            Ples izabraniPles = mtplesovi.vratiIzabraniPles(red);
            mtplesovi.getListaPlesova().remove(izabraniPles);
            mtplesovi.fireTableDataChanged();
        }
    }//GEN-LAST:event_btnObrisiPlesActionPerformed

    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        if(ispravno()){
           
            
            int sifra = Integer.parseInt(txtSifraKursa.getText());
            
            kurs.setSifraKursa(sifra);
            kurs.setNazivKursa(txtNazivKursa.getText());
            kurs.setCena(Double.parseDouble(txtCena.getText()));
            kurs.setOpis(txtOpis.getText());
            kurs.setAdministrator(admin);
            mtplesovi.setKurs(kurs);
            ModelTabelePlesovi mtplesovi = (ModelTabelePlesovi) tabelaPlesovi.getModel();
         //   tabelaPlesovi.setModel(mtplesovi);
          //  mtplesovi = (ModelTabelePlesovi) tabelaPlesovi.getModel();
            if(mtplesovi.getKurs() == null){
                System.out.println("NULL vraca u frmDodajKurs");
                
            }
                    
            if (mtplesovi.getKurs().getListaPlesova().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti bar jedan ples!","Greška!",JOptionPane.ERROR_MESSAGE);
                return;
            }
         
            
            try {
                kurs.setListaPlesova(mtplesovi.getKurs().getListaPlesova());
            if (Kontroler.getInstanca().dodajKurs(kurs)==true) {
                JOptionPane.showMessageDialog(this, "Sistem je zapamtio novi kurs.", "Uspešno!",JOptionPane.INFORMATION_MESSAGE);
                FrmGlavna.dodajRedUTabeluK(kurs);
                this.dispose();
            }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti kurs.", "Greška!",JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(FrmDodajKurs.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

