    private void btnDodajPlesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajPlesActionPerformed
         kurs.setSifraKursa(Integer.parseInt(txtSifraKursa.getText()));
        Ples noviPles = new Ples();
        noviPles.setNaziv(txtNazivPlesa.getText());
       
        
        noviPles.setSifraPlesa(vratiPlesove().size() +i);
        i++;
        if(!kurs.getListaPlesova().contains(noviPles)){
            kurs.dodajPles(noviPles);
            noviPles.setKurs(kurs);
            mtplesovi.dodajPles(noviPles);
                
            ModelTabelePlesovi mtsPlesovi =  (ModelTabelePlesovi) tabelaPlesovi.getModel();
            tabelaPlesovi.setModel(mtplesovi);// ovo je novi
            mtsPlesovi.fireTableDataChanged();
           

           
           
        }else{
            JOptionPane.showMessageDialog(this, "Већ постоји ставка са том шифром!","Грешка",JOptionPane.ERROR_MESSAGE);
        }
       
        
    }//GEN-LAST:event_btnDodajPlesActionPerformed

