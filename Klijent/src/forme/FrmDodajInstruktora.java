    private void napuniCB() {
        try {
            cbKurs.removeAllItems();
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Konstante.VRATI_KURSEVE);
            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            List<Kurs> kursevi = (List<Kurs>) so.getPodaci();
            
            for (Kurs k : kursevi) {
                cbKurs.addItem(k);
            }
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(FrmDodajInstruktora.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void srediFormu() {
        i = new Instruktor();
        i.setSifraInstruktora(vratiInstruktore().size()+1);
        txtSifra.setText(String.valueOf(i.getSifraInstruktora()));
        txtSifra.setEnabled(false);
    }

    private List<Instruktor> vratiInstruktore() {
        List<Instruktor> listaInstruktora = new LinkedList<>();
        try {
            listaInstruktora = Kontroler.getInstanca().vratiSveInstruktore();
            
        } catch (Exception ex) {
            Logger.getLogger(FrmDodajKurs.class.getName()).log(Level.SEVERE, null, ex);
        }
        return listaInstruktora;
    }

    private boolean ispravno() {
        if( txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty() || txtImejl.getText().isEmpty() ||
                txtBrojTelefona.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Morate uneti sva polja.", "Upozorenje!",JOptionPane.INFORMATION_MESSAGE);
            return false;
        }else{
        return true;
        }
    }

   
