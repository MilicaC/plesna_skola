    private void btnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajActionPerformed
        if(ispravno()){
         
            int sifraInstruktora = Integer.parseInt(txtSifra.getText());
            String imeI = txtIme.getText();
            String prezimeI = txtPrezime.getText();
            String imejlI = txtImejl.getText();
            String brTelefonaI = txtBrojTelefona.getText();
            mtk = (ModelTabeleKursevi) tabelaKursevi.getModel();
            List<Kurs> listaKurseva = mtk.vratiKurseve();
            Instruktor instruktor = new Instruktor(sifraInstruktora, imeI, prezimeI, imejlI, brTelefonaI, listaKurseva);
            if (listaKurseva.size() == 0) {
                JOptionPane.showMessageDialog(this, "Morate uneti bar jedan kurs!","Greška!",JOptionPane.ERROR_MESSAGE);
                return;
            }
         
            try {
            if (Kontroler.getInstanca().dodajInstruktora(instruktor)==true) {
                JOptionPane.showMessageDialog(this, "Sistem je zapamtion novog instruktora.", "Uspesno!",JOptionPane.INFORMATION_MESSAGE);
                mtk.fireTableDataChanged();
                FrmGlavna.dodajRedUTabeluI(instruktor);
                this.dispose();
            }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Sistem ne može da zapamti instruktora.", "Greška!",JOptionPane.ERROR_MESSAGE);
                Logger.getLogger(FrmDodajInstruktora.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSacuvajActionPerformed

