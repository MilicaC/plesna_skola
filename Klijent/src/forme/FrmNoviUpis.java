    private boolean dodajUpis(Upis upis) {
        
        System.out.println("USLO JE U DODAJ UPIS");
         try {
            boolean kreiran = Kontroler.getInstanca().dodajUpis(upis);
            if (kreiran == true) {
            // JOptionPane.showMessageDialog(this, "Логопед је успешно додат.", "Успешно!",JOptionPane.INFORMATION_MESSAGE);
                return true;
            }
            if(kreiran == false){
                System.out.println("KREIRAN JE FALSE ZAO MI JE");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Систем не може да запамти новог логопеда.", "Грешка!",JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(FrmNoviUpis.class.getName()).log(Level.SEVERE, null, ex);
        }
        return false;
    }
