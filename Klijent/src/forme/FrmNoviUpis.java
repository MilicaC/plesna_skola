    private void napuniCBPolaznici() {
         try {
            cbPolaznici.removeAllItems();
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Konstante.VRATI_POLAZNIKE);
            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            List<Polaznik> polaznici = (List<Polaznik>) so.getPodaci();
            
            for (Polaznik p : polaznici) {
                cbPolaznici.addItem(p);
            }
          //  cbPolaznici.setSelectedIndex(-1);
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(FrmDodajPolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    private void napuniCBKursevi() {
        try {
            cbKursevi.removeAllItems();
            KlijentskiZahtev kz = new KlijentskiZahtev();
            kz.setOperacija(Konstante.VRATI_KURSEVE);
            Komunikacija.getInstance().posaljiZahtev(kz);
            ServerskiOdgovor so = Komunikacija.getInstance().primiOdgovor();
            List<Kurs> kursevi = (List<Kurs>) so.getPodaci();
            
            for (Kurs k : kursevi) {
                cbKursevi.addItem(k);
            }
           // cbKursevi.setSelectedIndex(-1);
        } catch (IOException | ClassNotFoundException ex) {
            Logger.getLogger(FrmDodajPolaznika.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

